DELIMITER //
CREATE FUNCTION EJERCICIO12(AN INT) RETURNS INT
READS SQL DATA DETERMINISTIC
BEGIN
	DECLARE DIF INT;
    SELECT MAX(NE) - MIN(NE) INTO DIF FROM ENTREGA 
    WHERE YEAR(FECHA) = AN;
    RETURN DIF;
END// 
DELIMITER ;

SELECT EJERCICIO12(2023) AS 'DIFERENCIA ENTREGA';

SELECT * FROM ENTREGA;

